def main():
    with open(STEP2, encoding='utf-8') as f:
        step2 = json.load(f)
    with open(STEP3, encoding='utf-8') as f:
        step3 = json.load(f)

    existing_edges = set((e["from"], e["to"]) for e in step2["edges"])
    existing_priors = set()
    for did, val in step3["root_priors"].items():
        if did.startswith("D") and isinstance(val, dict) and val.get("cpt"):
            existing_priors.add(did)

    added_p = 0; added_e = 0
    for did, val in PRIORS.items():
        if val is None or did in existing_priors:
            continue
        parents, desc, cpt = val
        step3["root_priors"][did] = {"parents": parents, "description": desc, "cpt": cpt}
        added_p += 1
        for p in parents:
            if (p, did) not in existing_edges:
                step2["edges"].append({"from": p, "to": did,
                    "from_name": "age_group" if p == "R01" else "sex",
                    "to_name": did, "reason": desc, "onset_day_range": None})
                existing_edges.add((p, did))
                added_e += 1

    clin_e = 0
    for frm, to, fn, tn, reason in CLINICAL_EDGES:
        if (frm, to) not in existing_edges:
            step2["edges"].append({"from": frm, "to": to, "from_name": fn,
                "to_name": tn, "reason": reason, "onset_day_range": None})
            existing_edges.add((frm, to))
            clin_e += 1

    for vid, nop in NEW_NOISY_OR.items():
        if vid not in step3.get("noisy_or_params", {}):
            step3.setdefault("noisy_or_params", {})[vid] = nop
    for (child, parent), cpt in CLINICAL_CPTS.items():
        nop = step3.get("noisy_or_params", {}).get(child)
        if nop and parent not in nop.get("parent_effects", {}):
            nop.setdefault("parent_effects", {})[parent] = cpt

    step2["total_edges"] = len(step2["edges"])
    with open(STEP2, 'w', encoding='utf-8') as f:
        json.dump(step2, f, ensure_ascii=False, indent=2)
    with open(STEP3, 'w', encoding='utf-8') as f:
        json.dump(step3, f, ensure_ascii=False, indent=2)

    print(f"既存prior: {len(existing_priors)}, 新規: {added_p}, 合計: {len(existing_priors)+added_p}/345")
    print(f"Skip(パターンなし): {sum(1 for v in PRIORS.values() if v is None)}")
    print(f"R→D edges: {added_e}, 臨床辺: {clin_e}, Total edges: {step2['total_edges']}")

if __name__ == "__main__":
    main()
